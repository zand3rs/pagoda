function console_log() {
    if (CONFIG.debug) {
        console.log.apply(console, arguments);
    }
}

//-------------------------------------------------------------------------

function strPad(i,l,s) {
    var o = i.toString();
    if (!s) { s = '0'; }
    while (o.length < l) {
        o = s + o;
    }
    return o;
}

